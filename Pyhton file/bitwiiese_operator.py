# Common Bitwise Operators
# Operator	  Symbol	Description	Example (in binary)
# AND	       &	Sets each bit to 1 if both bits are 1	1101 & 1011 = 1001
# OR	       |	Sets each bit to 1 if at least one bit is 1	`1101	1011 = 1111`
# XOR	       ^	Sets each bit to 1 if only one bit is 1	1101 ^ 1011 = 0110
# NOT	       ~	Inverts all bits (1 becomes 0, 0 becomes 1)	~1101 = 0010 (in 4-bit)
# Left Shift   <<	Shifts bits to the left, adds 0s on the right	0010 << 1 = 0100
# Right Shift  >>	Shifts bits to the right, discards rightmost bits	0100 >> 1 = 0010