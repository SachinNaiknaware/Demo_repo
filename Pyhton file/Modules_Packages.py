# 1. Module म्हणजे काय?

# Module म्हणजे Python मधला एक फाइल असतो ज्यात काही कोड (functions, classes, variables) लिहिलेले असतात — हे इतर फाइलमध्ये वापरता येतात.
# फायदा असा की, कोड पुन्हा वापरता (reusable) येतो आणि प्रोजेक्ट व्यवस्थित व्यवस्थित ठेवता येतो.


# Different types of modules (मॉड्यूलचे प्रकार)
# Inbuilt modules → Python मध्ये आधीपासून उपलब्ध (उदा. math, os, sys)

# External modules → pip वापरून install करायचे (उदा. numpy, pandas)

# User-defined modules → स्वतः तयार केलेले .py फाइल्स



