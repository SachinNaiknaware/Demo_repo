# 2. Package म्हणजे काय?

# Package म्हणजे अनेक modules एकत्र करून ठेवण्याची पद्धत.
# Python मध्ये package म्हणजे एक फोल्डर (folder) असतो ज्यात अनेक .py फाइल्स (modules) असतात आणि त्या फोल्डरमध्ये __init__.py नावाची फाइल असते.

# ही फाइल Python ला सांगते की हा फोल्डर एक “package” आहे.

